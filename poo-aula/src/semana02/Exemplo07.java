package semana02;

public class Exemplo07 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println("\n---------------------------------------");
		
		for (int i = 10; i >= 0; i--) {
			System.out.print(i + " ");
		}
		
		System.out.println("\n---------------------------------------");
		
		for (char i = 'a'; i <= 'z'; i++) {
			System.out.print(i + " ");			
		}
		
		System.out.println("\n---------------------------------------");
		
		for (char i = 0; i <= 255; i++) {
			System.out.print(i + " ");			
		}
		

	}
	

}
